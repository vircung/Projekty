    private void btn_dodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dodajActionPerformed
        // TODO add your handling code here:
        if (studenci.size() == 50) {
            JOptionPane.showMessageDialog(this, "Lista studenÃ³w osiÄ…gneÅ‚a maksymalny rozmiar!", "UWAGA!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (WczytajStudentaJN() != null) {
            studenci.add(WczytajStudentaJN());
        }
        WyczyscPolaJN();
        UpdateListJN();
    }//GEN-LAST:event_btn_dodajActionPerformed

    private void UpdateListJN() {
        list_student.setListData(studenci.toArray());
        list_status.setText("IloÅ›Ä‡ studentÃ³w: " + studenci.size());
    }

    private void SelectionChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_SelectionChanged
        // TODO add your handling code here:
        btn_modyfikuj.setEnabled(true);
        selectedIndex = list_student.getSelectedIndex();
        if (selectedIndex > -1) {
            btn_dodaj.setEnabled(false);
            StudentJN s = (StudentJN) studenci.get(selectedIndex);
            txt_imie.setText(s.getImieJN());
            txt_nazwisko.setText(s.getNazwiskoJN());
            txt_srednia.setText(String.valueOf(s.getSredniaJN()));
        } else {
            btn_modyfikuj.setEnabled(false);
        }
    }//GEN-LAST:event_SelectionChanged

    private StudentJN WczytajStudentaJN() {
        StudentJN s = null;
        String imie = txt_imie.getText();
        String nazwisko = txt_nazwisko.getText();
        float srednia = 0;

        try {
            if ("".equals(imie)) {
                JOptionPane.showMessageDialog(this, "Nie podaÅ‚eÅ› imienia!", "UWAGA!", JOptionPane.WARNING_MESSAGE);
            }
            if ("".equals(nazwisko)) {
                JOptionPane.showMessageDialog(this, "Nie podaÅ‚eÅ› nazwiska!", "UWAGA!", JOptionPane.WARNING_MESSAGE);
                return null;
            }

            srednia = Float.parseFloat(txt_srednia.getText());

            s = new StudentJN(imie, nazwisko, srednia);
            return s;
        } catch (StudentWyjatekJN ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "UWAGA!", JOptionPane.WARNING_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Niepoprawna Å›rednia!", "UWAGA!", JOptionPane.WARNING_MESSAGE);
            return null;
        }
        return s;
    }
    private void btn_modyfikujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modyfikujActionPerformed
        // TODO add your handling code here:

        if (selectedIndex > -1) {
            if (WczytajStudentaJN() != null) {
                studenci.set(selectedIndex, WczytajStudentaJN());
            }
        }
        btn_dodaj.setEnabled(true);
        WyczyscPolaJN();
        UpdateListJN();
        btn_modyfikuj.setEnabled(false);
    }//GEN-LAST:event_btn_modyfikujActionPerformed
