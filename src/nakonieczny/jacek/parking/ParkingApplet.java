    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String marka = c_marka_txt.getText();
        String model = c_model_txt.getText();
        String kolor = c_kolor_txt.getText();
        String rej = c_rej_txt.getText();

        int rok = Integer.parseInt(c_rok_txt.getText());
        int wartosc = Integer.parseInt(c_wart_txt.getText());
        Integer kola = Integer.parseInt(c_kola_txt.getText());

        try {
            Samochod s = new SamochodCiezarowy(marka, model, kolor, rej, rok, kola, wartosc);

            samochody.add(s);
            log_txt.setText(log_txt.getText() + "Dodalem samochod ciężarowy \n");
        } catch (IllegalArgumentException ex) {
            ex.printStackTrace();
            log_txt.setText(log_txt.getText() + ex.getCause() + "\n");
        } catch (Exception ex) {
            ex.printStackTrace();
            log_txt.setText(log_txt.getText() + ex.getMessage() + "\n");
        } finally {
            c_marka_txt.setText("");
            c_model_txt.setText("");
            c_kolor_txt.setText("");
            c_rej_txt.setText("");

            c_rok_txt.setText("");
            c_kola_txt.setText("");
            c_wart_txt.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

