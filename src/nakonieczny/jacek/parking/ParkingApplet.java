    private void OnFocus(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_OnFocus
        // TODO add your handling code here:
        StringBuilder sb_o = new StringBuilder();
        StringBuilder sb_c = new StringBuilder();
        jta_osobowe.setText("");
        jta_ciezarowe.setText("");
        for (int i = 0; i < samochody.size(); i++) {
            if (samochody.get(i).getTyp() == TypSamochodu.Ciezarowy) {
                sb_c.append(samochody.get(i).toString());
            } else if (samochody.get(i).getTyp() == TypSamochodu.Osobowy) {
                sb_o.append(samochody.get(i).toString());
            }
        }

        jta_osobowe.setText(sb_o.toString());
        jta_ciezarowe.setText(sb_c.toString());
    }//GEN-LAST:event_OnFocus
